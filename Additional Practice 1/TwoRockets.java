
public class TwoRockets {

	public static void main(String[] args) {
		
		tri();
		box();
		System.out.println(" |United| |United|");
		System.out.println(" |States| |States|");
		box();
		tri();

	}
	static void tri() {
		System.out.println("    /\\       /\\");
		System.out.println("   /  \\     /  \\");
		System.out.println("  /    \\   /    \\");
		
	}
	static void box() {
		System.out.println(" +------+ +------+");
		System.out.println(" |      | |      |");
		System.out.println(" |      | |      |");
		System.out.println(" +------+ +------+");

		
	}
}

